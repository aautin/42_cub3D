#include "libft.h"

int	ft_atoi(const char *nbr)
{
	int	i;
	int	r;
	int	s;

	i = 0;
	r = 0;
	s = 1;
	while ((nbr[i] >= 9 && nbr[i] <= 13) || nbr[i] == ' ')
		i++;
	if (nbr[i] == '-' || nbr[i] == '+')
	{
		if (nbr[i] == '-')
			s *= -1;
		i++;
	}
	while (nbr[i] > 47 && nbr[i] < 58)
	{
		r = r * 10 + (nbr[i] - '0');
		i++;
	}
	return (r * s);
}
